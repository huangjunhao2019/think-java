public class Alphabet{
	public static void main(String[] args){
		System.out.println("Roman alphabet");
		for(char c='A';c<='Z';c++){
			System.out.print(c+" ");
		}
		System.out.println();
		System.out.println("Greek alphabet");
		for(int i=913;i<=937;i++){
			System.out.println((char)i);
		}System.out.println();
	}
}

